    <p>Although there are traditional winners at “Lysts on the Lake”, the most sought out and redeeming quality which continues to attract competitors from around the world is the camaraderie, hospitality and overall enjoyment of the event.  All the competitors treat one another with respect and admiration for their abilities required to participate in the world’s largest jousting competition.

    <p>There is one recognition given at the “Lysts on the Lake” which focuses less on horsemanship, skill with weapons or other physical abilities but more on individual honour. "Lysts on the Lake" is proud to have <a href="www.eafk.org">EarlyAct FirstKnight</a> as our premiere sponsor and together we are helping to educate the public on the importance of honour in our daily lives.  At the “Lysts on the Lake”, the most sought after award is The Award of Honour.  This award recognizes the individual who most clearly demonstrates the traditional virtues of chivalry throughout the tournament.

    <p>Depending on the historical time and location, there are quite a variety of definitions for the virtues of chivalry.  However, the main virtues are: Honour, Loyalty, Valor, Courtesy (especially towards women), Generosity (of spirit as well as material wealth) and Humility.

    <p>Dawn Hemphill is our Lady of Honour for "Lysts on the Lake". Together, she and her staff of ladies will be watching and listening to the behavior of the competitors and determining which one best demonstrates the medieval virtues of chivalry. Based on her observations and the recommendations from others, she will have the very difficult decision of choosing the one person who best deserves the Award of Honour.

    <p>For more information on the medieval, as well as more modern ideals of chivalry, please visit EarlyAct FirstKnight at <a href="www.eafk.org">www.eafk.org</a> and Chivalry today at  <a href="www.chivalrytoday.com">www.chivalrytoday.com</a>.
