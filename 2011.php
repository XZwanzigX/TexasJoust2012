<p>The first "Lysts on the Lake" was held from April 29 - May 1, 2011 at Richard Garriott's estate, Castleton, on Lake Travis in Austin, TX. A total of 23 competitors participated in at least one of the equestrian skills divisions, most competitors participated in all three equestrian skills divisions. The large number of competitors, 18 of which participated in the Joust a'Plaisance, made "Lysts on the Lake 2011" the <b>largest competitive jousting tournament ever held in the world</b> according to the <a href="http://users.skynet.be/hackamores/EJL/LEJ.htm">International Jousting League</a>.</p>

<p><img src="img/2011/DustinBothCabbages.jpg" style="width: 200px; float: left; margin: 0 5px 5px 0;" alt="<b>Dustin Stephens</b>(photo by Lauren Black)">


    <p><div class="centered-element"><b>Mounted Skill at Arms</b></div>

    <p>In the Mounted Skill at Arms competition, several different aspects of mounted combat were tested. <b>Dustin Stephens</b> managed to earn perfect scores in tilting at rings, the cut and thrust, the thrown spear and ground targeting; and he achieved the highest number of rotations on the quintain. With such impressive all around abilities, he obviously deserved to win the Mounted Skill at Arms competition.
</p>

