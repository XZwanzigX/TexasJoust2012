<p><img src="img/2011/DaveLadybugMelee.jpg" style="width: 150px; float: right; margin: 0 0 10px 10px;" alt="<b>Dave Wise</b>(Photo by Jay Baum)">


    <p><div class="centered-element"><b>Melee a'Cheval</b></div>

    <p>The Melee a'Cheval involved 16 mounted combatants all fighting against each other at the same time. The competition depended on each competitor's honor in acknowledging the hits against him/her and withdrawing when he/she had received five SOLID blows. <b>Dave Wise</b> managed to overcome all other competitors before receiving five solid hits, and therefore he won the competition.
</p>

<br><br>
<p><img src="img/2011/SteveWinsJoust.jpg" style="width: 140px; float: left; margin: 0 10px 10px 0;" alt="<b>Steve Hemphill</b>(photo by AzulOx)">

<p><div class="centered-element"><b>Joust a'Plaisance</b></div>

<p>Eighteen competitors participated in the Joust a'Plaisance, making "Lysts on the Lake 2011" the largest competitive jousting tournament in North America. Each competitor held the field as Tenan and faced four challengers who ran three tilting passes against them as Venans. In each tilting pass, it was possible to win up to four points.

<p>Because some competitors rode as Venans more frequently than others, a simple sum of points would not have been fair. Therefore the average of the points per pass were calculated in order to create the score representing each competitor's jousting ability. When all was said and done, <b>Steve Hemphill</b>, with a score of 3.0952, was the competitor who came closest to having the perfect score of 4.0 and was therefore the winner of the Joust a'Plaisance.

<br><br><br>
<p><div class="centered-element"><b>Award of Honour</b></div>

<p>During the entire tournament, the Lady of Honour, Dawn Alee Hemphill, watched the competitors and evaluated their behavior according to the traditional virtues of chivalry. She also listened to what the competitors and other participants said regarding each jouster. At the end of the tournament, she had seen and heard of honourable behavior from most, if not all, of the competitors, but there was one jouster who stood out above the rest as the person who most clearly demonstrated the traditional virtues of chivalry. Thus, she chose <b>Dustin Stephens</b> to receive the Award of Honour.
</p>

<p><div class="centered-element"><b>Overall Champion</b></div>

<p>In order to become the overall Champion of Lysts on the Lake 2011, not only did a competitor have to do well in all of the physical aspects of the tournament, he or she also had to demonstrate honourable behavior throughout the competition. Because of his abilities in all the different aspects of equestrian skill tested during the tournament(winning the Mounted Skill at Arms competition, coming in sixth in the Melee a'Cheval, and earning 2.6667 points in the Joust a'Plaisance), as well as his demonstrations of impeccable honour, <b>Dustin Stephens</b> was determined to be the overall Champion of Lysts on the Lake 2011.
