<p>"Lysts on the Lake" was held April 29 - May 1, 2011 on the lake front estate of Austin game developer and entrepreneur, Richard Garriott, in Austin, Texas. 24 competitors participated in the event with 18 as part of the Joust a'Plaisance.  This made Lysts on the Lake 2011, the largest competitive joust in the world (according to the <a href="http://users.skynet.be/hackamores/EJL/LEJ.htm">International Jousting League</a>).</p>

<p><img src="img/2011/DustinBothCabbages.jpg" style="width: 200px; float: left; margin: 0 5px 5px 0;" alt="<b>Dustin Stephens</b>(photo by Lauren Black)">


    <p><div class="centered-element"><b>Mounted Skill at Arms</b></div>

    <p>In the Mounted Skill at Arms competition, several different aspects of mounted combat were tested. <b>Dustin Stephens</b> managed to earn perfect scores in tilting at rings, cut and thrust, thrown spear, ground targeting and the highest number of rotations on the quintain. With such impressive abilities he easily won the Mounted Skill at Arms competition.
</p>

<br><br>
<p><img src="img/2011/DaveLadybugMelee.jpg" style="width: 150px; float: right; margin: 0 0 10px 10px;" alt="<b>Dave Wise</b>(Photo by Jay Baum)">


    <p><div class="centered-element"><b>Melee a'Cheval</b></div>

    <p>The Melee a'Cheval involved 16 mounted combatants all fighting one another in a large melee format. The competition depended on each competitor's honor in acknowledging the blows against him/her and withdrawing when he/she had received five solid blows. <b>Dave Wise</b> managed to overcome all the competitors to win this event.
</p>

<br><br>
<p><img src="img/2011/SteveWinsJoust.jpg" style="width: 140px; float: left; margin: 0 10px 10px 0;" alt="<b>Steve Hemphill</b>(photo by AzulOx)">

<p><div class="centered-element"><b>Joust a'Plaisance</b></div>

<p>Eighteen competitors participated in the Joust a'Plaisance.  Each competitor held the field (as "tenans") and faced four challengers ("venans") by running three tilting passes against each. In each tilting pass, it was possible to win up to four points.  Some competitors rode as venans more frequently than others so a simple sum of points would not have accurately determined the winner. Therefore the average of the points per pass was calculated to determine the winner. <b>Steve Hemphill</b>, with a score of 3.0952, was the competitor who came closest to having a perfect score of 4.0 and, thus, was declared the winner of the Joust a'Plaisance.

<br><br><br>
<p><div class="centered-element"><b>Award of Honour</b></div>

<p>The Lady of Honour, <b>Dawn Hemphill</b>, and her staff of ladies, were tasked with watching the competitors to evaluate their behavior according to the traditional virtues of chivalry. Throughout the tournament she had heard of honourable behavior from the majority of the competitors.  However, there was one competitor who stood out above the rest as the person who most clearly demonstrated the traditional virtues of chivalry. Thus, she chose Dustin Stephens to receive the Award of Honour.
</p>

<p><div class="centered-element"><b>Overall Champion</b></div>

<p>In order to become the overall Champion of Lysts on the Lake 2011, a competitor not only had to do well in all of the physical aspects of the tournament but was required to demonstrate honourable behavior throughout the competition. Because of his skill on horseback (winning the Mounted Skill at Arms competition, placing 6th in the Melee a'Cheval and earning 2.6667 points in the Joust a'Plaisance), as well as his demonstrations of impeccable honour, <b>Dustin Stephens</b> was determined to be the overall Champion of Lysts on the Lake 2011.

